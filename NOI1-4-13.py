'''
描述
编写程序，计算下列分段函数y=f(x)的值。

y=-x+2.5; 0 <= x < 5

y=2-1.5(x-3)(x-3); 5 <= x < 10

y=x/2-1.5; 10 <= x < 20

输入
一个浮点数N,0 <= N < 20
输出
输出N对应的分段函数值：f(N)。结果保留到小数点后三位。
'''

N=eval(input())
if 0<=N<5:
    y=-N+2.5
elif 5<=N<10:
    y=2-1.5*(N-3)*(N-3)
else:
    y=N/2-1.5
print("{:.3f}".format(y))